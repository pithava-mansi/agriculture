    <h1>Agricultural Products Overview</h1>
    <div class="section">
        <h2>1. Crops:</h2>
        <ul>
            <li><strong>Grains:</strong> Wheat, rice, corn (maize), barley, oats, and sorghum.</li>
            <li><strong>Fruits:</strong> Apples, oranges, bananas, berries, grapes, and melons.</li>
            <li><strong>Vegetables:</strong> Potatoes, carrots, tomatoes, lettuce, and peppers.</li>
            <li><strong>Legumes:</strong> Beans, lentils, chickpeas, and peas.</li>
            <li><strong>Oilseeds:</strong> Soybeans, canola, sunflower seeds, and flaxseeds.</li>
            <li><strong>Root Crops:</strong> Sweet potatoes, yams, and beets.</li>
            <li><strong>Tubers:</strong> Potatoes and yams.</li>
        </ul>
    </div>
    <div class="section">
        <h2>2. Livestock Products:</h2>
        <ul>
            <li><strong>Meat:</strong> Beef, pork, chicken, lamb, and goat.</li>
            <li><strong>Dairy:</strong> Milk, cheese, yogurt, butter, and cream.</li>
            <li><strong>Eggs:</strong> Chicken eggs, duck eggs, and quail eggs.</li>
        </ul>
    </div>
    <div class="section">
        <h2>3. Fiber:</h2>
        <ul>
            <li><strong>Textile Fibers:</strong> Cotton, wool, and flax (linen).</li>
            <li><strong>Industrial Fibers:</strong> Hemp and jute.</li>
        </ul>
    </div>
    <div class="section">
        <h2>4. Specialty Crops:</h2>
        <ul>
            <li><strong>Nuts:</strong> Almonds, walnuts, cashews, and peanuts.</li>
            <li><strong>Spices:</strong> Black pepper, cinnamon, cloves, and turmeric.</li>
            <li><strong>Herbs:</strong> Basil, mint, rosemary, and cilantro.</li>
        </ul>
    </div>
    <div class="section">
        <h2>5. Plant-Based Products:</h2>
        <ul>
            <li><strong>Biofuels:</strong> Ethanol from corn or sugarcane, biodiesel from oilseeds.</li>
            <li><strong>Plant Oils:</strong> Olive oil, coconut oil, and soybean oil.</li>
            <li><strong>Sweeteners:</strong> Sugar from sugarcane or sugar beets, honey.</li>
        </ul>
    </div>
    <div class="section">
        <h2>6. Forage and Feed:</h2>
        <ul>
            <li><strong>Animal Feed:</strong> Hay, silage, and feed grains for livestock.</li>
        </ul>
    </div>
    <div class="section">
        <h2>7. Others:</h2>
        <ul>
            <li><strong>Flowers and Ornamental Plants:</strong> Used for decoration and landscaping.</li>
            <li><strong>Medicinal Plants:</strong> Herbs and plants used for their therapeutic properties.</li>
        </ul>
    </div>
    <div class="section">
        <h2>Importance of Agricultural Products:</h2>
        <ul>
            <li><strong>Food Supply:</strong> Essential for human nutrition and sustenance.</li>
            <li><strong>Economic Contribution:</strong> Major source of income for farmers and a significant part of many economies.</li>
            <li><strong>Raw Materials:</strong> Basis for various industries, including food processing, textiles, and biofuels.</li>
            <li><strong>Sustainability:</strong> Plays a role in sustainable practices and conservation efforts through organic farming and eco-friendly production methods.</li>
        </ul>
